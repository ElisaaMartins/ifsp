// Exercício 1: Operações Aritméticas Básicas

#include <iostream>

using namespace std;

int main () {
	double valor1, valor2;
	
	cout << "Insira o primero valor: " << endl;
	cin >> valor1;
	
	cout << "Insira o segundo valor: " << endl;
	cin >> valor2;
	
	cout << "Soma: " << valor1 + valor2 << endl;
	cout << "Produto: " << valor1 * valor2 << endl;
	cout << "Diferença: " << valor1 - valor2 << endl;
	cout << "Quociente: " << valor1 / valor2 << endl;
}
